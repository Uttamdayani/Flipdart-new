                    <script>
    // Wait for the DOM to be fully loaded
    document.addEventListener('DOMContentLoaded', function () {
        // Select all elements with class 'storage-item'
        let buttons = document.querySelectorAll('.storage-item');

        // Check if there is a stored value in local storage
        let storedStorageName = localStorage.getItem('activeStorage');

        if (!storedStorageName) {
            // If no stored value, set the first button's value in local storage by default
            let firstButton = buttons[0];
            if (firstButton) {
                let storageName = firstButton.querySelector('.storage-name').textContent;
                localStorage.setItem('activeStorage', storageName);
            }
        } else {
            // If there is a stored value, find the button with the matching 'storage-name' value
            let activeButton = Array.from(buttons).find(button =>
                containsText(button.querySelector('.storage-name'), storedStorageName)
            );

            // If a matching button is found, add the 'active' class
            if (activeButton) {
                activeButton.classList.add('active');
            }
        }

        // Add click event listener to each button
        buttons.forEach(button => {
            button.addEventListener('click', function () {
                // Remove 'active' class from all buttons
                buttons.forEach(btn => btn.classList.remove('active'));

                // Add 'active' class to the clicked button
                this.classList.add('active');

                // Get the text content of the 'storage-name' span inside the clicked button
                let storageName = this.querySelector('.storage-name').textContent;

                // Store the 'storage-name' value in local storage
                localStorage.setItem('activeStorage', storageName);
            });
        });
    });

    // Helper function to check if an element contains specific text
    function containsText(element, text) {
        return element.textContent.indexOf(text) !== -1;
    }
</script>


                
